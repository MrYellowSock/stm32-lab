char sendBuff[100] = { 0 };
void cleverSend(UART_HandleTypeDef huart, char *tosend) {
	while (__HAL_UART_GET_FLAG(&huart,UART_FLAG_TC) == RESET) {
	}
	sendBuff[0] = 0;
	strcat(sendBuff, tosend);
	HAL_UART_Transmit(&huart, (uint8_t*) sendBuff, strlen(sendBuff),
	HAL_MAX_DELAY);
}

uint8_t Rx_uart3[5];
uint8_t Rx_uart6[5];
float intensity[3] = { 0,0,0 };
void changeIntensity(int index, float change) {
	intensity[index] += change;
	if (intensity[index] > 1.0)
		intensity[index] = 0;

	char msg[50];
	sprintf(msg, "RED:%.3d%% GREEN:%.3d%% BLUE:%.3d%%\n", (int) (intensity[0] * 100.0),
			(int) (intensity[1] * 100.0), (int) (intensity[2] * 100.0));
	cleverSend(huart3, msg);
}
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
	if (huart->Instance == USART3) {
		switch (Rx_uart3[0]) {
		case 'r':
			changeIntensity(0, 0.2);
			break;
		case 'g':
			changeIntensity(1, 0.2);
			break;
		case 'b':
			changeIntensity(2, 0.2);
			break;
		}
		HAL_UART_Receive_IT(huart, Rx_uart3, 1);
	}
}
